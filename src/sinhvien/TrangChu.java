    public boolean isEmptyStudent() {
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Mã Sinh Viên!");
            return false;
        }
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Họ Tên!");
            return false;
        }
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Ngày Sinh!");
            return false;
        }
        if (jDateChooser1.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "Ngày Sinh Không Hợp Lệ!");
            return false;
        }
        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Quê Quán!");
            return false;
        }
        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Số Điện Thoại!");
            return false;
        }
        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Lớp!");
            return false;
        }
        if (jTextField8.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Niên Khóa!");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Chọn Ảnh");
            return false;
        }
        return true;
    }

